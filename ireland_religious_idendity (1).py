def pro_clean(x):
    if pd.isna(x): return ""
    x = str(x).upper()
    x = re.sub(r'\(.*\)', '', x)
    for word in ["COUNTY", "CITY", "CO.", "CO ", "URB", "RUR"]:
        x = x.replace(word, "")
    x = x.replace("'", "").replace(".", "").replace("-", " ")
    x = x.replace("Θ", "É").replace("┴", "Á")
    x = x.strip()

    encoding_fixes = {
    "Θ": "É", "┴": "Á", "≤": "Ó", "·": "Ú", "Φ": "Í",
    "SSN": "ÁN",  # e.g. CLOCHSSN -> CLOCHÁN
    "BHSSN": "BHÁN", "CORRSSN": "CORRÁN", "CNOCSSN": "CNOCÁN",
    "CROMPSSN": "CROMPÁN",
}
    for bad, good in encoding_fixes.items():
        x = x.replace(bad, good)

    county_map = {
        # English variants
        "NORTH TIPPERARY":        "TIPPERARY",
        "SOUTH TIPPERARY":        "TIPPERARY",
        "DUN LAOGHAIRE/RATHDOWN": "DÚN LAOGHAIRE RATHDOWN",
        # Irish county names → English (to match GIS file)
        "AN MHÍ":                 "MEATH",
        "CATHAIR NA GAILLIMHE":   "GALWAY",
        "CIARRAÍ":                "KERRY",
        "CORCAIGH":               "CORK",
        "DÚN LAOGHAIRE RATHDOWN": "DÚN LAOGHAIRE RATHDOWN",
        "DUN LAOGHAIRE RATHDOWN": "DÚN LAOGHAIRE RATHDOWN",
        "DUN LAOGHAIRE/RATHDOWN": "DÚN LAOGHAIRE RATHDOWN",
        "DUN LAOGHAIRE": "DÚN LAOGHAIRE RATHDOWN",
        "DÚN NA NGALL":           "DONEGAL",
        "GAILLIMH":               "GALWAY",
        "MAIGH EO":               "MAYO",
        "PORT LÁIRGE":            "WATERFORD",
        "TIPPERARY":              "TIPPERARY",
    }
    # x = strip_accents(x)
    return county_map.get(x, x)




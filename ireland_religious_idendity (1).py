def clean(df, year):
    df = df.copy()
    if debugDeep:
      print(f"\n[DEBUG] Cleaning year {year}")
      print("Columns BEFORE:", df.columns.tolist())

    if "Electoral Division" in df.columns:
        df = df.rename(columns={"Electoral Division": "ed"})
    elif "CSO Electoral Divisions 2022" in df.columns:
        df = df.rename(columns={"CSO Electoral Divisions 2022": "ed"})

    df.columns = df.columns.str.lower()
    df["year"] = year

    # Filter for 'both sexes' if 'sex' column exists and contains 'both sexes'
    if 'sex' in df.columns and 'both sexes' in df['sex'].unique():
        df = df[df['sex'] == 'both sexes']
    elif 'sex' in df.columns and debugDeep:
        print(f"[DEBUG] 'both sexes' not found in 'sex' column for year {year}. Keeping all sexes.")

    # Filter for 'all ages' if 'age group' column exists and contains 'all ages'
    if 'age group' in df.columns and 'all ages' in df['age group'].unique():
        df = df[df['age group'] == 'all ages']
    elif 'age group' in df.columns and debugDeep:
        print(f"[DEBUG] 'all ages' not found in 'age group' column for year {year}. Keeping all age groups.")

    if debugDeep:
      print("Columns AFTER:", df.columns.tolist())
      print("Sample ED values:", df["ed"].head(3).tolist())

    return df



def split_ed(x):
    # This regex removes the 5-digit code like ", 08028"
    x_clean = re.sub(r',\s*\d{5}$', '', str(x))
    parts = x_clean.split(",")
    name = parts[0].strip().upper()
    # Normalize County: "Co. Laois" -> "LAOIS"
    county = parts[1].replace("Co.", "").replace("County", "").strip().upper() if len(parts) > 1 else None
    return pd.Series([name, county])


